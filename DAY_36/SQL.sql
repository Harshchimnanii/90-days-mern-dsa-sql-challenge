-- - [ ]  **Topic:** Boyce-Codd Normal Form (BCNF).
-- - [ ]  **Concept:** A table is in BCNF if for every functional dependency $X \rightarrow Y$, $X$ is a Super Key. It is a stronger version of 3NF.
-- - [ ]  **Interview Question:** "What is the difference between 3NF and BCNF?" (Hint: BCNF does not allow dependencies where the left side is not a super key).